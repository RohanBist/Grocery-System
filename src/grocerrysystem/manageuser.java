    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a user to delete.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Get the user ID and Role from the selected row
    String userID = jTable1.getValueAt(selectedRow, 0).toString();
    String userRole = jTable1.getValueAt(selectedRow, 4).toString(); // Role column index (Adjust if needed)

    // Confirm deletion with an extra warning for Admin users
    String message = "Are you sure you want to delete this user?";
    if (userRole.equalsIgnoreCase("Admin")) {
        message = "WARNING: You are about to delete an Admin user! This action cannot be undone. Proceed?";
    }

    int confirmation = JOptionPane.showConfirmDialog(this, message, "Confirm Deletion", JOptionPane.YES_NO_OPTION);

    if (confirmation == JOptionPane.YES_OPTION) {
        String SUrl = "jdbc:mysql://localhost:3306/userdata"; // Database URL
        String SUser = "root"; // Database username
        String SPass = ""; // Database password

        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // Load MySQL Driver
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();

            // SQL Query to delete user
            String query = "DELETE FROM user WHERE ID = '" + userID + "'";
            int rowsAffected = st.executeUpdate(query);

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "User deleted successfully!");
                
                // Refresh the table
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.removeRow(selectedRow);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete user. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            st.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println("Error: " + e.getMessage());
        }
    } // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

