    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String SUrl, SUser, SPass;
    SUrl = "jdbc:mysql://localhost:3306/userdata"; // Change to your actual database name
    SUser = "root";
    SPass = "";

    try {
        Class.forName("com.mysql.cj.jdbc.Driver"); // Load MySQL driver
        Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
        Statement st = con.createStatement();
        
        // Get the selected role from the combo box
        String selectedRole = (String) jComboBox1.getSelectedItem();
        
        // Create the SQL query to fetch user details based on the selected role
        String query = "SELECT ID, full_name, email, password, role FROM user WHERE role = '" + selectedRole + "'"; 
        ResultSet rs = st.executeQuery(query);

        // Get table model and clear existing data
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        int increment=1;
        // Loop through the results and add to table
        while (rs.next()) {
            String ID = rs.getString("ID");
            String fullName = rs.getString("full_name");
            String email = rs.getString("email");
            String password = rs.getString("password");
            String role = rs.getString("role");

            model.addRow(new Object[]{increment, fullName, email, password, role});
            increment++;
        }

        // Close resources
        rs.close();
        st.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(new JFrame(), "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        System.out.println("Error: " + e.getMessage());
    }         // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

