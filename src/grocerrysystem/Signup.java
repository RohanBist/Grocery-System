    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
  String fullName, email, password, confirmPassword, role;
    String SUrl, SUser, SPass;
    SUrl = "jdbc:mysql://localhost:3306/userdata"; // Corrected MySQL URL
    SUser = "root";
    SPass = "";

    // Retrieve input values
    fullName = jTextField1.getText(); 
    email = jTextField2.getText();
    password = new String(jPasswordField1.getPassword()); // Get password from jPasswordField1
    confirmPassword = new String(jPasswordField2.getPassword()); // Get confirm password from jPasswordField2
     role = (String) jComboBox1.getSelectedItem();

    // Validate inputs
    if (fullName.isEmpty()) {
        JOptionPane.showMessageDialog(new JFrame(), "Full Name is required", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (email.isEmpty()) {
        JOptionPane.showMessageDialog(new JFrame(), "Username is required", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (password.isEmpty()) {
        JOptionPane.showMessageDialog(new JFrame(), "Password is required", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (confirmPassword.isEmpty()) {
        JOptionPane.showMessageDialog(new JFrame(), "Confirm Password is required", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!password.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(new JFrame(), "Passwords do not match", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        // If all validations pass, proceed with database insertion
        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // Load MySQL driver
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();

            // Create the SQL query
            String query = "INSERT INTO user(full_name, email, password,role) " +
                           "VALUES('" + fullName + "', '" + email + "', '" + password + "','"+role+"')";

            // Execute the query
            st.executeUpdate(query);

            // Clear input fields
            jTextField1.setText("");
            jTextField2.setText("");
            jPasswordField1.setText("");
            jPasswordField2.setText("");

            // Show success message
            JOptionPane.showMessageDialog(null, "New account has been created successfully!");

            // Close resources
            st.close();
            con.close();
        } catch (Exception e) {
            // Handle errors
            JOptionPane.showMessageDialog(new JFrame(), "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println("Error: " + e.getMessage());
        }
    } // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

