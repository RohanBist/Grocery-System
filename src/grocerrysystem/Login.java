    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 String Email, Password, query, fname = null, passDb = null, role = null;
    String SUrl, SUser, SPass;
    SUrl = "jdbc:MySQL://localhost:3306/userdata";
    SUser = "root";
    SPass = "";

    // Hardcoded credentials
    String hardcodedEmail = "admin";
    String hardcodedPassword = "123";

    Email = jTextField1.getText();
    Password = new String(jPasswordField1.getPassword());

    // Check hardcoded credentials first
    if (Email.equals(hardcodedEmail) && Password.equals(hardcodedPassword)) {
        JOptionPane.showMessageDialog(new JFrame(), "Login Successful as Admin", "Success", JOptionPane.INFORMATION_MESSAGE);
        admindashboard adminDashboard = new admindashboard();
        adminDashboard.setVisible(true);
        adminDashboard.pack();
        adminDashboard.setLocationRelativeTo(null); 
        this.dispose();
        return; // Exit function to prevent further execution
    }

    int notFound = 0;
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
        Statement st = con.createStatement();

        if ("".equals(Email)) {
            JOptionPane.showMessageDialog(new JFrame(), "Username is required", "Error", JOptionPane.ERROR_MESSAGE);
        } else if ("".equals(Password)) {
            JOptionPane.showMessageDialog(new JFrame(), "Password is required", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            query = "SELECT * FROM user WHERE email= '" + Email + "'";
            ResultSet rs = st.executeQuery(query);
            
            while (rs.next()) {
                passDb = rs.getString("password");
                fname = rs.getString("Full_Name");
                role = rs.getString("role");
                notFound = 1;
            }

            if (notFound == 1 && Password.equals(passDb)) {
                if ("Admin".equals(role)) {
                    JOptionPane.showMessageDialog(new JFrame(), "Logged in as Admin", "Success", JOptionPane.INFORMATION_MESSAGE);
                    admindashboard adminDashboard = new admindashboard();
                    adminDashboard.setVisible(true);
                    adminDashboard.pack();
                    adminDashboard.setLocationRelativeTo(null); 
                    this.dispose();
                } else if ("Staff".equals(role)) {
                    JOptionPane.showMessageDialog(new JFrame(), "Logged in as Staff", "Success", JOptionPane.INFORMATION_MESSAGE);
                    Welcome HomeFrame = new Welcome();
                    HomeFrame.setVisible(true);
                    HomeFrame.pack();
                    HomeFrame.setLocationRelativeTo(null); 
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(new JFrame(), "Logged in with unknown role", "Info", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(new JFrame(), "Incorrect Username or Password", "Error", JOptionPane.ERROR_MESSAGE);
            }

            jPasswordField1.setText("");
        }
    } catch (Exception e) {
        System.out.println("Error!" + e.getMessage());
    }     // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

